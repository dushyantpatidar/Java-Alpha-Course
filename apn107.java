class apn107{
    public static void main(String[]args){
        System.out.println(5&6); // AND Operator are 
        System.out.println(5|6); // OR Operator are 
        System.out.println(5^6); // XOR Operator are
        System.out.println(~5); // XOR Operator are 
        System.out.println(5<<2); // Left Shift Operator are 
         System.out.println(6>>1); // Right Shift Operator are 
    }
}